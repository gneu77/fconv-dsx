GAUSS<n_dim,T>::GAUSS(T orig, T *sig) {
    for (int i=0; i<n_dim; ++i) {
        sigma[i] = *(sig+i);
        origin[i] = orig;
    }
}

template<int n_dim,class T>
GAUSS<n_dim,T>::GAUSS(T *orig, T sig) {
    for (int i=0; i<n_dim; ++i) {
        sigma[i] = sig;
        origin[i] = *(orig+i);
    }
}


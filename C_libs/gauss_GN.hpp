//fuer Dreiecksglaettung: (gleichschenkliges Dreieck mit Basis = 0,4)
//gibt Flaeche von k bis l
float get_triangle_area(float dst,float k,float l) {
    float lterm = dst - l;
    float kterm = dst - k;
    if (kterm < 0.00001) { //komplett auf der rechten Dreiecksseite (=> spiegeln)
        lterm = -1. * kterm;
        kterm = l - dst;
        if (kterm < 0.2) kterm = 0.2 - kterm;
        else kterm = 0.;
        lterm = 0.2 - lterm;
        return (((lterm*lterm)-(kterm*kterm)) / 0.08);
    } else if (lterm < 0.) { //mittleres bin
        lterm = get_triangle_area(dst,dst,l);
        kterm = get_triangle_area(dst,k,dst);
        return (lterm + kterm);
    } else { //komplett auf der linken Seite
        if (kterm < 0.2) kterm = 0.2 - kterm;
        else kterm = 0.;
        lterm = 0.2 - lterm;
        return (((lterm*lterm)-(kterm*kterm)) / 0.08);
    }
}

